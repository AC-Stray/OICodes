int t, MOD, n;
LL read(){
    ll n = 0; int f = 1; char ch = getchar();
    while('0' > ch || ch > '9'){
        if(ch == '-') f *= -1;
        ch = getchar();
    }
    while('0' <= ch && ch <= '9'){
        n = (n << 1) + (n << 3) + ch-'0';
        ch = getchar();
    }
    return f * n;
}
Void write(ll x){
    if(x/10) write(x/10);
    putchar(x%10+'0');
}
Void input() {}
template<typename Type, typename... Types>
Void input(Type& arg, Types&... args){
    arg = read();
    input(args...);
}
int main(){
    input(t, MOD);
    while(t--){
        input(n);
        switch(n){
            case 1: cout << 3     % MOD; break;
            case 2: cout << 15    % MOD; break;
            case 3: cout << 123   % MOD; break;
            case 4: cout << 1635  % MOD; break;
            case 5: cout << 35043 % MOD; break;
        }
    }
    return 0;
}